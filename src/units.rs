//! Custom types used in the program for readability

/// Custom type for temperature
pub type Kelvin = f64;

/// Custom type for pressure
pub type Pascal = f64;

/// Pub type for mass
pub type Gram = f64;

/// density type
pub type GramCm3 = f64;

/// Time type
pub type Second = f64;

/// rod position type (0-100 in code)
pub type RodPosition = u8;

/// percent (also 0-100 only in code)
pub type Percent = u8;

/// heat capacities of materials
pub type JoulesPerGramKelvin = f64;
